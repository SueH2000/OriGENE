#!/usr/bin/env python
"""Extract mutation features from a VEP-annotated VCF.

The script expects a VCF generated by Ensembl VEP with `--vcf --symbol --canonical --pick` and dbNSFP fields. The Snakemake workflow in this folder performs VCF preprocessing and VEP annotation before calling this extractor.

The output keeps one row per gene from `Final_CDS_lengths.csv`, including genes
without classified mutations, so the feature matrix remains aligned with DORGE
and OriGENE gene-level inputs.

Implementation details:
    Ratio features use a fixed pseudocount of 1.0 by default. PolyPhen-2 HDIV score is used for HiFI/LoFI classification. Recurrent missense fraction is computed as the proportion of missense mutations occurring at protein positions with count > 1.
"""

from __future__ import annotations

import argparse
import math
from collections import defaultdict
from pathlib import Path
from typing import Dict, Iterable, List, Optional, Sequence, Tuple

import numpy as np
import pandas as pd


MISSENSE_TERMS = {"missense_variant"}
SILENT_TERMS = {"synonymous_variant"}
NONSENSE_TERMS = {"stop_gained"}
FRAMESHIFT_TERMS = {"frameshift_variant"}
SPLICING_TERMS = {"splice_acceptor_variant", "splice_donor_variant"}

POLYPHEN_FIELD_CANDIDATES = (
    "Polyphen2_HDIV_score",
    "PolyPhen2_HDIV_score",
    "PolyPhen_HDIV_score",
    "PolyPhen",
)

FEATURE_COLUMNS = [
    "Silent mutations/kb",
    "log Total N missense mutations",
    "log Total N LoF mutations",
    "log Total N of splicing mutations",
    "Missense mutations/kb",
    "LoF mutations/kb",
    "Missense entropy (Entropy for missense mutations)",
    "LoF/Silent ratio",
    "Splicing/silent ratio",
    "Missense/silent ratio",
    "HiFI missense/LoFI missense ratio",
    "LoF/benign ratio",
    "Splicing/benign ratio",
    "Missense/benign ratio",
    "HiFI Missense/benign ratio (Missense damaging/benign ratio)",
    "PolyPhen-2 score",
    "LoF/total mutations",
    "Missense/total mutations",
    "Splicing/total mutations",
    "LoF/missense mutations",
    "Silent fraction",
    "Nonsense fraction",
    "Recurrent missense fraction",
    "Frameshift indel fraction",
    "Inframe indel fraction",
    "Lost start and stop fraction",
    "Inactivating fraction",
    "Nonsilent/silent ratio",
]


def read_cds_lengths(path: Path) -> Tuple[Dict[str, float], List[str]]:
    """Read CDS lengths and return an uppercase gene-to-length mapping."""
    df = pd.read_csv(path)
    required = {"Gene", "CDS_length"}
    missing = required.difference(df.columns)
    if missing:
        raise ValueError(f"CDS length CSV is missing required columns: {sorted(missing)}")

    df = df[["Gene", "CDS_length"]].copy()
    df["Gene"] = df["Gene"].astype(str).str.upper().str.strip()
    df["CDS_length"] = pd.to_numeric(df["CDS_length"], errors="coerce")
    df = df.dropna(subset=["Gene", "CDS_length"])
    df = df[df["CDS_length"] > 0]
    df = df.drop_duplicates(subset=["Gene"], keep="first")

    genes = df["Gene"].tolist()
    return dict(zip(df["Gene"], df["CDS_length"])), genes


def parse_csq_fields(raw_header: str) -> List[str]:
    """Extract the CSQ field order from a VEP VCF header."""
    csq_header = next(
        (line for line in raw_header.split("\n") if line.startswith("##INFO=<ID=CSQ")),
        None,
    )
    if csq_header is None:
        raise ValueError(
            "No INFO/CSQ field found. Run VEP with --vcf before feature extraction."
        )
    if "Format: " not in csq_header:
        raise ValueError("Could not parse the VEP CSQ format string from the VCF header.")
    return csq_header.split("Format: ", 1)[1].rstrip('">').split("|")


def field_index(fields: Sequence[str], key: str) -> Optional[int]:
    """Return the index of a CSQ key, or None if the key is absent."""
    return fields.index(key) if key in fields else None


def first_present_index(fields: Sequence[str], keys: Iterable[str]) -> Optional[int]:
    """Return the index for the first available CSQ key from a candidate list."""
    for key in keys:
        idx = field_index(fields, key)
        if idx is not None:
            return idx
    return None


def has_any_consequence(consequence: str, terms: set[str]) -> bool:
    """Match consequence terms using the notebook's substring check."""
    return any(term in consequence for term in terms)


def extract_mutation_features(
    vcf_path: Path,
    cds_lengths_csv: Path,
    hifi_threshold: float = 0.85,
    lofi_threshold: float = 0.15,
    pseudocount: float = 1.0,
    progress_every: int = 10_000,
) -> pd.DataFrame:
    """Parse a VEP-annotated VCF and return the notebook-style feature table."""
    try:
        from cyvcf2 import VCF
    except ImportError as exc:
        raise ImportError(
            "cyvcf2 is required. Install the mutation feature environment first."
        ) from exc

    cds_length, all_genes_ordered = read_cds_lengths(cds_lengths_csv)
    all_genes = set(all_genes_ordered)

    gene_counts = defaultdict(lambda: defaultdict(int))
    missense_pos_counter = defaultdict(lambda: defaultdict(int))
    polyphen_scores = defaultdict(list)

    vcf = VCF(str(vcf_path))
    csq_fields = parse_csq_fields(vcf.raw_header)

    consequence_idx = field_index(csq_fields, "Consequence")
    symbol_idx = field_index(csq_fields, "SYMBOL")
    protein_pos_idx = field_index(csq_fields, "Protein_position")
    polyphen_idx = first_present_index(csq_fields, POLYPHEN_FIELD_CANDIDATES)

    if consequence_idx is None or symbol_idx is None:
        raise ValueError("Required CSQ fields `Consequence` and `SYMBOL` were not found.")

    required_idx = max(
        idx for idx in (consequence_idx, symbol_idx, protein_pos_idx, polyphen_idx) if idx is not None
    )

    print(f"Loaded CDS lengths for {len(all_genes_ordered):,} genes.")
    print(
        "CSQ indices: "
        f"Consequence={consequence_idx}, SYMBOL={symbol_idx}, "
        f"Protein_position={protein_pos_idx}, PolyPhen={polyphen_idx}"
    )

    n_variants = 0
    for variant in vcf:
        n_variants += 1
        if progress_every and n_variants % progress_every == 0:
            print(f"Processed {n_variants:,} variants...")

        csq_raw = variant.INFO.get("CSQ")
        if not csq_raw:
            continue

        for entry in str(csq_raw).split(","):
            fields = entry.split("|")
            if len(fields) <= required_idx:
                continue

            gene = fields[symbol_idx].upper().strip()
            if not gene or gene not in all_genes:
                continue

            consequence = fields[consequence_idx]
            counts = gene_counts[gene]
            counts["total"] += 1

            if has_any_consequence(consequence, MISSENSE_TERMS):
                counts["missense"] += 1

                if protein_pos_idx is not None and len(fields) > protein_pos_idx:
                    position = fields[protein_pos_idx]
                    if position:
                        missense_pos_counter[gene][position] += 1

                if polyphen_idx is not None and len(fields) > polyphen_idx:
                    try:
                        score = float(fields[polyphen_idx])
                        polyphen_scores[gene].append(score)
                        if score >= hifi_threshold:
                            counts["HiFI"] += 1
                        elif score <= lofi_threshold:
                            counts["LoFI"] += 1
                    except ValueError:
                        pass

            elif has_any_consequence(consequence, SILENT_TERMS):
                counts["silent"] += 1
            elif has_any_consequence(consequence, NONSENSE_TERMS):
                counts["nonsense"] += 1
            elif has_any_consequence(consequence, FRAMESHIFT_TERMS):
                counts["frameshift"] += 1
            elif has_any_consequence(consequence, SPLICING_TERMS):
                counts["splice"] += 1
            elif "start_lost" in consequence:
                counts["start_lost"] += 1
            elif "stop_lost" in consequence:
                counts["stop_lost"] += 1
            elif "inframe_insertion" in consequence or "inframe_deletion" in consequence:
                counts["inframe_indel"] += 1
            elif "nonstop" in consequence:
                counts["nonstop"] += 1

    rows = []
    for gene in sorted(all_genes):
        counts = gene_counts[gene]
        cds_kb = cds_length[gene] / 1000.0

        total = counts["total"]
        missense = counts["missense"]
        silent = counts["silent"]
        nonsense = counts["nonsense"]
        frameshift = counts["frameshift"]
        splice = counts["splice"]
        start_lost = counts["start_lost"]
        stop_lost = counts["stop_lost"]
        inframe = counts["inframe_indel"]
        nonstop = counts["nonstop"]
        hifi = counts["HiFI"]
        lofi = counts["LoFI"]

        lof = nonsense + frameshift
        inactivating = frameshift + splice + start_lost + nonstop
        benign = silent + lofi

        entropy = 0.0
        positions = list(missense_pos_counter[gene].values())
        if sum(positions) > 0:
            probabilities = np.array(positions) / sum(positions)
            entropy = float(-np.sum(probabilities * np.log2(probabilities)))

        if missense > 0:
            recurrent_count = sum(c for c in missense_pos_counter[gene].values() if c > 1)
            recurrent_fraction = recurrent_count / missense
        else:
            recurrent_fraction = 0.0

        rows.append(
            {
                "Gene": gene,
                "Silent mutations/kb": silent / cds_kb,
                "log Total N missense mutations": math.log10(missense + pseudocount),
                "log Total N LoF mutations": math.log10(lof + pseudocount),
                "log Total N of splicing mutations": math.log10(splice + pseudocount),
                "Missense mutations/kb": missense / cds_kb,
                "LoF mutations/kb": lof / cds_kb,
                "Missense entropy (Entropy for missense mutations)": entropy,
                "LoF/Silent ratio": (lof + pseudocount) / (silent + pseudocount),
                "Splicing/silent ratio": (splice + pseudocount) / (silent + pseudocount),
                "Missense/silent ratio": (missense + pseudocount) / (silent + pseudocount),
                "HiFI missense/LoFI missense ratio": (hifi + pseudocount) / (lofi + pseudocount),
                "LoF/benign ratio": (lof + pseudocount) / (benign + pseudocount),
                "Splicing/benign ratio": (splice + pseudocount) / (benign + pseudocount),
                "Missense/benign ratio": (missense + pseudocount) / (benign + pseudocount),
                "HiFI Missense/benign ratio (Missense damaging/benign ratio)": (
                    hifi + pseudocount
                )
                / (benign + pseudocount),
                "PolyPhen-2 score": (
                    float(np.mean(polyphen_scores[gene])) if polyphen_scores[gene] else np.nan
                ),
                "LoF/total mutations": lof / total if total else 0.0,
                "Missense/total mutations": missense / total if total else 0.0,
                "Splicing/total mutations": splice / total if total else 0.0,
                "LoF/missense mutations": (lof + pseudocount) / (missense + pseudocount),
                "Silent fraction": silent / total if total else 0.0,
                "Nonsense fraction": nonsense / total if total else 0.0,
                "Recurrent missense fraction": recurrent_fraction,
                "Frameshift indel fraction": frameshift / total if total else 0.0,
                "Inframe indel fraction": inframe / total if total else 0.0,
                "Lost start and stop fraction": (start_lost + stop_lost) / total if total else 0.0,
                "Inactivating fraction": inactivating / total if total else 0.0,
                "Nonsilent/silent ratio": (total - silent + pseudocount) / (silent + pseudocount),
            }
        )

    df = pd.DataFrame(rows, columns=["Gene"] + FEATURE_COLUMNS)
    mutated_genes = sum(1 for gene in gene_counts if gene_counts[gene]["total"] > 0)
    print(f"Finished. Total variants parsed: {n_variants:,}")
    print(f"Genes with at least one matched mutation: {mutated_genes:,} / {len(all_genes_ordered):,}")
    top_genes = sorted(
        (gene for gene in gene_counts if gene_counts[gene]["total"] > 0),
        key=lambda gene: gene_counts[gene]["total"],
        reverse=True,
    )[:10]
    if top_genes:
        print("Top 10 mutated genes and their mutation counts:")
        for gene in top_genes:
            print(f"  {gene}: {gene_counts[gene]['total']} mutations")
    return df


def write_outputs(
    df: pd.DataFrame,
    output_dir: Path,
    sample_id: str,
) -> Path:
    """Write the raw gene-level mutation feature table."""
    output_dir.mkdir(parents=True, exist_ok=True)
    raw_path = output_dir / f"mutation_features_{sample_id}.csv"
    df.to_csv(raw_path, index=False)
    return raw_path


def build_arg_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(
        description="Extract gene-level mutation features from a VEP-annotated VCF."
    )
    parser.add_argument("--vcf", required=True, type=Path, help="VEP-annotated VCF or VCF.GZ.")
    parser.add_argument(
        "--cds-lengths",
        required=True,
        type=Path,
        help="CSV with columns Gene and CDS_length in base pairs.",
    )
    parser.add_argument("--sample-id", required=True, help="Sample name used in output filenames.")
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=Path("results/mutation_features"),
        help="Directory for mutation feature CSV outputs.",
    )
    parser.add_argument("--hifi-threshold", type=float, default=0.85)
    parser.add_argument("--lofi-threshold", type=float, default=0.15)
    parser.add_argument("--pseudocount", type=float, default=1.0)
    parser.add_argument(
        "--progress-every",
        type=int,
        default=10_000,
        help="Print progress every N variants; set to 0 to disable.",
    )
    return parser


def main() -> None:
    args = build_arg_parser().parse_args()
    df = extract_mutation_features(
        vcf_path=args.vcf,
        cds_lengths_csv=args.cds_lengths,
        hifi_threshold=args.hifi_threshold,
        lofi_threshold=args.lofi_threshold,
        pseudocount=args.pseudocount,
        progress_every=args.progress_every,
    )
    path = write_outputs(df, args.output_dir, args.sample_id)
    print(f"Saved {path}")


if __name__ == "__main__":
    main()
